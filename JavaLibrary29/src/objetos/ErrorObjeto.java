package objetos;

/**
 *
 * @author enzoq
 */
public class ErrorObjeto  {

    protected String nombre;
    protected int codigo;

    public ErrorObjeto(){
        
    }
    public ErrorObjeto(String nombre, int codigo) {
        this.nombre = nombre;
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    @Override
    public String toString() {
        return "Error{" + "nombre=" + nombre + ", codigo=" + codigo + '}';
    }
    
    

}
